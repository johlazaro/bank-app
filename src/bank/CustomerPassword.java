    private void passwordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordFieldKeyReleased
        // TODO add your handling code here:
        char[] chr=passwordField.getPassword();
        String password=new String(chr);
        String databasepassword=sq.getAccountOwner(accountNumberField.getText());
        if("new".compareTo(databasepassword)==0){
            if(password.length()<4){
                passwordMessage.setText("Weak Password");
                passwordMessage.setForeground(Color.RED);
            } 
            if(password.length()>4&&password.length()<7){
                passwordMessage.setText("Normal Password");
                passwordMessage.setForeground(new Color(0,0,0));
            }
            if(password.length()>7){
                passwordMessage.setText("Strong Password");
                passwordMessage.setForeground(new Color(0,102,51));
            }
            if(password.isEmpty())
                 passwordMessage.setText("");
        }
        
    }//GEN-LAST:event_passwordFieldKeyReleased

    private void confirmPasswordFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_confirmPasswordFieldKeyReleased
        // TODO add your handling code here:
        String password=new String(passwordField.getPassword());
        String confirmPassword=new String(confirmPasswordField.getPassword());
        if(password.length()<confirmPassword.length()){
            confirmPasswordMessage.setText("Unmatch Password");
            confirmPasswordMessage.setForeground(Color.RED);
        }
        if(confirmPassword.isEmpty())
            confirmPasswordMessage.setText("");
        if(password.length()>confirmPassword.length())
            confirmPasswordMessage.setText("");
    }//GEN-LAST:event_confirmPasswordFieldKeyReleased

