    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        // TODO add your handling code here:
        sq=new Sql();
        String accountNumber=accountNumberField.getText();
        String password=new String(passwordField.getPassword());
        try{
            String encryptedPassword=sq.getAccountOwner(accountNumber);
            byte[] decryptedPassword=Base64.getDecoder().decode(encryptedPassword);
            String password1=new String(decryptedPassword);
            if(password.equals(password1)){
                confirmPasswordMessage.setText("");
                new CustomerHomePage().setVisible(true);
                this.dispose();
            }else{
                passwordMessage.setText("Incorrect Password");
                passwordField.setText("");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Account does not Exist");
            accountNumberField.setText("");
        }
        
    }//GEN-LAST:event_signInButtonActionPerformed

