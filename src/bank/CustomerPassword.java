    public void check(){
         sq=new Sql();
        String accountNumber=accountNumberField.getText();
        try{
            String password=sq.getAccountOwner(accountNumber);
            if("new".compareTo(password)==0){
                passwordField.setVisible(true);
                confirmPasswordField.setVisible(true);
                enterPasswordLabel.setVisible(true);
                enterConfirmPassowordLabel.setVisible(true);
                signUpButton.setVisible(true);
                okTickLabel.setVisible(true);
                passwordField.requestFocus();
                
            }else{
                passwordField.setVisible(true);
                okTickLabel.setVisible(true);
                enterPasswordLabel.setVisible(true);
                signInButton.setVisible(true);
                passwordField.requestFocus();
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Account does not Exist","Login info",JOptionPane.ERROR_MESSAGE);
            signInButton.setVisible(false);
            okTickLabel.setVisible(false);
            passwordField.setVisible(false);
            accountNumberField.setText("");
            enterPasswordLabel.setVisible(false);
        }
    }
    public void signUpSendButton(){
        sq=new Sql();
        String accountNumber=accountNumberField.getText();
        String password1=new String(passwordField.getPassword());
        String password2=new String(confirmPasswordField.getPassword());
        String password = Base64.getEncoder().encodeToString(password1.getBytes());
        if(password1.equals(password2)){
            confirmPasswordMessage.setText("");
            sq.setNewPassword(password, accountNumber);
            new CustomerHomePage().setVisible(true);
            this.dispose();
        }else
        confirmPasswordMessage.setText("Unmatch Password");
    }

