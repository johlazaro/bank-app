    private void depositButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_depositButtonActionPerformed
        // TODO add your handling code here:
        sql=new Sql();
        String accountnumber=accountNumber.getText();
        String dte=dateAndTime.getText();
        String refNo=referenceNumber.getText();
        try{
            if(amountDeposited.getText().isEmpty()){
                depositedAmountLabel.setText("Please Enter Amount");
            }else{
                int option=JOptionPane.showConfirmDialog(null,"CONFIRM TRANSACTION","Deposit Info",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                if(option==JOptionPane.YES_OPTION){
                    double amountdeposited=(double)Integer.parseInt(amountDeposited.getText());
                    double accountBalance=sql.selectAccountBalance(accountnumber);
                    double totalBalance=accountBalance+amountdeposited;
                    sql.updateBalance(totalBalance,accountnumber);
                    sql.insertIntoDeposit(refNo,amountdeposited,dte,totalBalance,accountnumber);
                    //request new Transaction 
                    int opt=JOptionPane.showConfirmDialog(null,"  Deposit Successful\nYou Need To Continue ","Deposit info",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                    if(opt==JOptionPane.YES_OPTION){
                        amountDeposited.setText("");
                        deposit.setText("");
                        totalbalance.setText("");
                        availableBalance.setText(Double.toString(sql.selectAccountBalance(accountnumber)));
                        referenceNumber.setText(referenceNumberRandomGenerator());
                    }
                    else{
                        this.reset();
                    }
                }
            }
        
        }catch(NumberFormatException ex){
            
        }
    }//GEN-LAST:event_depositButtonActionPerformed

    private void amountDepositedKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_amountDepositedKeyReleased
        // TODO add your handling code here:
        sql=new Sql();
        double amountdeposited,accountBalance,totalBalance;
        String accountnumber=accountNumber.getText();
        try{
            depositedAmountLabel.setText(" ");
            amountdeposited=(double)Integer.parseInt(amountDeposited.getText());
            accountBalance=sql.selectAccountBalance(accountnumber);
            totalBalance=accountBalance+amountdeposited;
            deposit.setText(amountDeposited.getText());
            totalbalance.setText(Double.toString(totalBalance));
        }catch(NumberFormatException ex){
            //JOptionPane.showMessageDialog(null,"Bad number format");
            amountDeposited.setText("");
            depositedAmountLabel.setText("Enter Decimal number");
            deposit.setText("");
        }
    }//GEN-LAST:event_amountDepositedKeyReleased

