    public static String referenceNumberRandomGenerator() {
        int length=10;
	Random random = new Random();
	StringBuilder sb = new StringBuilder();
	while (sb.length() < length) {
		sb.append(Integer.toHexString(random.nextInt()));
	}
	return sb.toString();
    }
    public void checkButton(){
        File image;
        sql=new Sql();
        String accountnumber=accountNumber.getText();
        accountsno=new ArrayList<>();
        accountsno=sql.selectAllCustomer();
        try{
            if(accountsno.contains(accountnumber)){
                accountMessage.setText("");
                image=sql.selectCustomerImage(accountnumber);
                String accountname=sql.selectAllAccount(accountnumber);
                double accountBalance=sql.selectAccountBalance(accountnumber);
                okTick.setVisible(true);
                accountName.setText(accountname);
                availableBalance.setText(Double.toString(accountBalance));
                customerPhoto.setIcon(new ImageIcon(new ImageIcon(image.getAbsolutePath()).getImage().getScaledInstance(customerPhoto.getWidth(),customerPhoto.getHeight(),Image.SCALE_SMOOTH)));
                referenceNumber.setText(referenceNumberRandomGenerator());
                amountDeposited.requestFocus();
                new TransactionTime().start();
                amountDeposited.setEditable(true);
            }else{
                accountMessage.setText("Account doesn't Exist");
                accountNumber.setText("");
                this.reset();
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
