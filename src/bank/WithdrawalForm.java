    private void withdrawalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawalButtonActionPerformed
        // TODO add your handling code here:
        sql=new Sql();
        String accountnumber=accountNumber.getText();
        String refNo=referenceNumber.getText();
        try{
            String transactionDate=dateAndTime.getText();
            if(withdrawalAmount.getText().isEmpty()){
                withdrawalAmaountLabel.setText("Please Enter Amount");
            }else{
                int option=JOptionPane.showConfirmDialog(null,"CONFIRM TRANSACTION","Withdrawal Info",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                if(option==JOptionPane.YES_OPTION){
                    double amountWithdrawal=(double)Integer.parseInt(withdrawalAmount.getText());
                    double accountBalance=sql.selectAccountBalance(accountnumber);
                    double totalBalance=accountBalance-amountWithdrawal;
                    sql.updateBalance(totalBalance,accountnumber);
                    sql.insertIntoWithdrawal(accountnumber,refNo,transactionDate,amountWithdrawal,totalBalance);
                    //JOptionPane.showMessageDialog(null,"Deposit Successful","Deposit info",JOptionPane.INFORMATION_MESSAGE);
                    int opt=JOptionPane.showConfirmDialog(null,"  Withdrawal Successful\nDo you need to Continue ?","Withdrawal info",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
                    if(opt==JOptionPane.YES_OPTION){
                        withdrawalAmount.setText("");
                        withdrawal.setText("");
                        totalbalance.setText("");
                        availableBalance.setText(Double.toString(sql.selectAccountBalance(accountnumber)));
                        referenceNumber.setText(referenceNumberRandomGenerator());
                    }
                    else{
                        this.reset();
                    }
                }
            }
     
        }catch(NumberFormatException ex){
            
        }
    }//GEN-LAST:event_withdrawalButtonActionPerformed

