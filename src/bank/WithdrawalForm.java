    private void withdrawalAmountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_withdrawalAmountKeyReleased
        // TODO add your handling code here:
        sql=new Sql();
        double amountWithdrwal,accountBalance,totalBalance;
        String accountnumber=accountNumber.getText();
        try{
            withdrawalAmaountLabel.setText(" ");
            amountWithdrwal=(double)Integer.parseInt(withdrawalAmount.getText());
            accountBalance=sql.selectAccountBalance(accountnumber);
            if(accountBalance<amountWithdrwal){
                JOptionPane.showMessageDialog(null,"No enough Money","Withdrawal Info",JOptionPane.ERROR_MESSAGE);
                withdrawalAmount.setText("");
                withdrawal.setText("");
                totalbalance.setText("");
            }
            else{
                totalBalance=accountBalance-amountWithdrwal;
                withdrawal.setText(withdrawalAmount.getText());
                totalbalance.setText(Double.toString(totalBalance));
              }
        }catch(NumberFormatException ex){
            //JOptionPane.showMessageDialog(null,"Bad number format");
            withdrawalAmount.setText("");
            //withdrawalAmaountLabel.setText("Enter Decimal number");
            withdrawal.setText("");
        }
    }//GEN-LAST:event_withdrawalAmountKeyReleased

